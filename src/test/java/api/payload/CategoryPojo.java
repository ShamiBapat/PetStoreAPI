package api.payload;

public class CategoryPojo {
	
	private int id;
	private String name;
	
	
	public int getID() {
		return id;
	}
	public int setID(int id) {
		return this.id = id;
	}
	public String getName() {	
		return name;
	}
	public String setName(String name) {
		return this.name = name;
	}
	

}
