package api.payload;

public class CategoryPojo {
	
	private int id;
	private String name;
	
	
	public int getCategoryID() {
		return id;
	}
	public int setCategoryID(int id) {
		return this.id = id;
	}
	public String getCategoryName() {
		return name;
	}
	public String setCategoryName(String name) {
		return this.name = name;
	}
	

}
